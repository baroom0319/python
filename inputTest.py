print(num1, "x", num2, "=", numMul)
numDiv = num1 / num2
print(num1, "/", num2, "=", numDiv)
numSqu = num1 ** num2
print(num1, "**", num2, "=", numSqu)
numHal = num1 % num2
print(num1, "%", num2, "=", numHal)
